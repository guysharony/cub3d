#include "../../includes/parser.h"

int		getmap_width(char *map)
{
	int		a;
	int		b;

	a = 0;
	b = 0;
	while (map[a])
	{
		if (map[a] != ' ')
			b++;
		a++;
	}
	return (b);
}
